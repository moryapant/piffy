import { ref } from 'vue'
import {
  Dialog,
  DialogPanel,
  DialogTitle,
  TransitionChild,
  TransitionRoot,
} from '@headlessui/vue'

const props = defineProps({
  isOpen: {
    type: Boolean,
    default: false,
  },
  title: {
    type: String,
    default: 'Alert',
  },
  message: {
    type: String,
    default: 'This is an alert message.',
  },
  okText: {
    type: String,
    default: 'OK',
  },
})

const emit = defineEmits(['close'])

